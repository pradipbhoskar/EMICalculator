package Model;

public class YearModel {
	
	private int year;   //no of years
	private float oyp; //one year principle amount paid 
	private float oyi; //one year interest paid 
	private float oyemi;    //yearly emi
	private float oybr; //one year balance ramaining 
	private float oylp; //one year loan paid
	private float oylpper;  //one year loan paid percentage
	
	
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	public float getOyp() {
		return oyp;
	}
	public void setOyp(float oyp) {
		this.oyp = oyp;
	}
	public float getOyi() {
		return oyi;
	}
	public void setOyi(float oyi) {
		this.oyi = oyi;
	}
	public float getOyemi() {
		return oyemi;
	}
	public void setOyemi(float oyemi) {
		this.oyemi = oyemi;
	}
	public float getOybr() {
		return oybr;
	}
	public void setOybr(float oybr) {
		this.oybr = oybr;
	}
	public float getOylp() {
		return oylp;
	}
	public void setOylp(float oylp) {
		this.oylp = oylp;
	}
	public float getOylpper() {
		return oylpper;
	}
	public void setOylpper(float oylpper) {
		this.oylpper = oylpper;
	}

	
	
}
